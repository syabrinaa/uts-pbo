    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
   try{
    Class.forName("com.mysql.jdbc.Driver");
    Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/pbo","root","");
    String str="insert into user values(?,?,?,?,?)";
    PreparedStatement stmt=koneksi.prepareStatement(str);
    
    stmt.setString(1, id_user.getText());
    stmt.setString(2, username.getText());
    stmt.setString(3, password.getText());
    stmt.setString(4, nama.getText());
    stmt.setString(5, role_id.getText());
    stmt.executeUpdate();
    JOptionPane.showMessageDialog(null,"Berhasil menambahkan Data!");
    }catch(Exception e) {
    JOptionPane.showMessageDialog(null,e);
    dispose();
        }
    dispose();
    halamanUser pindah = new halamanUser();
    pindah.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

