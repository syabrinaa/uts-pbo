    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String sql="select * from user where id_user=?";
             try {
                 stmt = konek.prepareStatement(sql);
                 stmt.setString(1,id_user.getText());
                 ResultSet rs=stmt.executeQuery();
                 
                 if(rs.next()){
                     id_user.setText(rs.getString("id_user"));
                     username.setText(rs.getString("username"));
                     password.setText(rs.getString("password"));
                     nama.setText(rs.getString("nama"));
                     role_id.setText(rs.getString("role_id"));
                 }
                 else{
                     JOptionPane.showMessageDialog(this, "Data Not Found");
                 }
            
             } catch (SQLException ex) {
                 Logger.getLogger(Modify.class.getName()).log(Level.SEVERE, null, ex);
             }
       
    }//GEN-LAST:event_jButton1ActionPerformed

